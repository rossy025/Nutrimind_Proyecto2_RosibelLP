    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        // TODO add your handling code here:
        try {
            int edad     = (jSpinnerEdad.getValue() instanceof Integer) ? (Integer) jSpinnerEdad.getValue() : 0;
            int alturaCm = Integer.parseInt(jTextFieldAltura.getText().trim());
            double peso  = Double.parseDouble(jTextFieldPeso.getText().trim());

            Paciente p = new Paciente();
            p.setCedula(jTextFieldCedula.getText().trim());
            p.setNombre(jTextFieldNombre.getText().trim());
            p.setEdad(edad);
            p.setSexo((String) jComboBoxSexo.getSelectedItem());
            p.setAltura((double) alturaCm);
            p.setPeso(peso);
            p.setActividadFisica((String) jComboBoxActividad.getSelectedItem());

            controller.actualizar(p);
            JOptionPane.showMessageDialog(this, "Paciente actualizado.");
            cargarTabla();
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Altura y peso deben ser numéricos.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButtonActualizarActionPerformed

    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        // TODO add your handling code here:
        try {
            String ced = jTextFieldBuscarCedula.getText();
            Paciente p = controller.buscarPorCedula(ced);
            if (p == null) { JOptionPane.showMessageDialog(this, "No existe un paciente con esa cédula."); return; }
            // llenar formulario
            jTextFieldCedula.setText(p.getCedula());
            jTextFieldNombre.setText(p.getNombre());
            jSpinnerEdad.setValue(p.getEdad());
            jComboBoxSexo.setSelectedItem(p.getSexo());
            jTextFieldAltura.setText(String.valueOf(p.getAltura()));
            jTextFieldPeso.setText(String.valueOf(p.getPeso()));
            jComboBoxActividad.setSelectedItem(p.getActividadFisica());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
    }
        
    }//GEN-LAST:event_jButtonBuscarActionPerformed

    private void jButtonLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLimpiarActionPerformed
        // TODO add your handling code here:
        jTextFieldBuscarCedula.setText("");
        limpiar();
        jTablePacientes.clearSelection();
        cargarTabla();
    }//GEN-LAST:event_jButtonLimpiarActionPerformed

    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        // TODO add your handling code here:
        String ced = jTextFieldCedula.getText().trim();
        if (ced.isEmpty()) { JOptionPane.showMessageDialog(this, "Seleccione un paciente."); return; }
        int op = JOptionPane.showConfirmDialog(this, "¿Eliminar paciente " + ced + "?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (op != JOptionPane.YES_OPTION) return;

        try {
            boolean ok = controller.eliminar(ced);   
            if (ok) {
                JOptionPane.showMessageDialog(this, "Paciente eliminado.");
                jButtonLimpiarActionPerformed(null);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar (revise dependencias).");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    
    }//GEN-LAST:event_jButtonEliminarActionPerformed

