    private void cargarTabla() {
        try {
            DefaultTableModel m = controller.cargarTabla();   // del controller
            jTablePacientes.setModel(m);
            jTablePacientes.setRowHeight(22);
            jTablePacientes.getTableHeader().setReorderingAllowed(false);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar: " + ex.getMessage());
        }
    }
    
    private void limpiar() {
        jTextFieldCedula.setText("");
        jTextFieldNombre.setText("");
        jSpinnerEdad.setValue(1);
        jComboBoxSexo.setSelectedIndex(0);
        jTextFieldAltura.setText("");
        jTextFieldPeso.setText("");
        jComboBoxActividad.setSelectedIndex(0);
    }
